// Canonical point size for the menu glyph; @1x/2x/3x rasters keep it crisp.
const SCALES = [
  { suffix: "@1x", px: 24 },
  { suffix: "@2x", px: 48 },
  { suffix: "@3x", px: 72 },
];

const contentsJson = (name) => ({
  images: SCALES.map((s) => ({
    filename: `${name}${s.suffix}.png`,
    idiom: "universal",
    scale: `${s.px / 24}x`,
  })),
  info: { author: "expo", version: 1 },
  properties: {
    "template-rendering-intent": "template",
    "preserves-vector-representation": false,
  },
});

for (const [name, markup] of Object.entries(LOGOS)) {
  const dir = join(OUT, `provider-${name}.imageset`);
  if (!existsSync(dir)) mkdirSync(dir, { recursive: true });
  for (const scale of SCALES) {
    const png = await sharp(Buffer.from(markup))
      .resize(scale.px, scale.px, {
        fit: "contain",
        background: { r: 0, g: 0, b: 0, alpha: 0 },
      })
      .png()
      .toBuffer();
    writeFileSync(join(dir, `${name}${scale.suffix}.png`), png);
  }
  writeFileSync(
    join(dir, "Contents.json"),
    `${JSON.stringify(contentsJson(name), null, 2)}\n`,
  );
  console.log(`✓ provider-${name}`);
}
